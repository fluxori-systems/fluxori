/**
 * Types for spacing values
 */

/**
 * Standard spacing size names that can be used throughout the application
 */
export type SpacingSize = 'xs' | 'sm' | 'md' | 'lg' | 'xl';

/**
 * Valid spacing values that can be used for margin, padding, gap, etc.
 */
export type SpacingValue = SpacingSize | number | string;